class Solution(object):
	def uniquePathsWithObstacles(self,obstacleGrid):
		"""
		:type obstacleGrid: List[List[int]]
		:rtype: int
		"""
		m=len(obstacleGrid)
		n=len(obstacleGrid[0])
		board=[]
		isSearch=[]
		for i in range(m):
			l=[0 for j in range(n)]
			board.append(l)
			isSearch.append([False for j in range(n)])
		self.path(m-1,n-1,board,obstacleGrid,isSearch)
		return board[m-1][n-1]

	def path(self,m,n,board,obstacleGrid,isSearch):
		#print board
		if obstacleGrid[m][n]==1:
			board[m][n]=0
			isSearch[m][n]=True
			return
		if m==0 and n==0:
			board[m][n]=1
			isSearch[m][n]=True
		else:
			a=0
			b=0
			if n-1>=0:
				if not isSearch[m][n-1]:
					self.path(m,n-1, board,obstacleGrid,isSearch)
				a=board[m][n-1]

			if m-1>=0:
				if not isSearch[m-1][n]:
					self.path(m-1,n, board,obstacleGrid,isSearch)
				b=board[m-1][n]

			board[m][n]=a+b
			isSearch[m][n]=True

