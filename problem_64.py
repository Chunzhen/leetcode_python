class Solution(object):
	def minPathSum(self,grid):
		"""
		:type grid: List[List[int]]
		:rtype: int
		"""
		m=len(grid)
		n=len(grid[0])
		board=[]
		for i in range(m):
			l=[0 for j in range(n)]
			board.append(l)
		self.path(m-1,n-1,board,grid)
		return board[m-1][n-1]

	def path(self,m,n,board,grid):
		if m==0 and n==0:
			board[m][n]=grid[m][n]
		else:
			a=1000
			b=1000
			if n-1>=0:
				if board[m][n-1]==0:
					self.path(m,n-1, board,grid)
				a=board[m][n-1]

			if m-1>=0:
				if board[m-1][n]==0:
					self.path(m-1,n, board,grid)
				b=board[m-1][n]
			board[m][n]=min(a+grid[m][n],b+grid[m][n])

